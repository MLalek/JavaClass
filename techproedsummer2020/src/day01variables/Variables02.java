package day01variables;

public class Variables02 {

	public static void main(String[] args) {
		float salary = 12f; //12 = 12.0
		System.out.println(salary);
		
		float money = 12.45F; // f or F both ara posibble
		System.out.println(money);
		
		double length = 3.987; // instead of putting f at the and,
								// you can change data type to double
		System.out.println(length); // 3.546
		
		double size = 1.23f;
		System.out.println(size);

	}

}
