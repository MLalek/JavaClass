package day01variables;

public class Variables01 {

	public static void main(String[] args) {
		
		//if you use println Java will print it as a next line
		System.out.print("Hello Mehmet!");
		System.out.println("Hello World!");
		
		
		//if you use "" Java prints the text directly on the concole
		boolean retired = true;
		System.out.println(retired);
		
		boolean javaDifficult = false;
		System.out.println(javaDifficult);
		
		int age = 18;
		System.out.println(age);
		
		float salary = 2.2f;
		System.out.println(salary);
		
		double pi = 10.24444446;
		System.out.println(pi);	
		
		//char tek krakter
		char initial = 'S';
		System.out.println(initial);
		
		String name = "Mehmet";
		System.out.println(name);
		

	}

}
