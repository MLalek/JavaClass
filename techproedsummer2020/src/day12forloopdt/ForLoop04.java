package day12forloopdt;

public class ForLoop04 {

	public static void main(String[] args) {
		// Find the product(multiplication) of first 5 counting number and print the
		//product on the console
		//If they ask you product then multiplication if sum then addition 
		
		int product =1;
		
		for(int i=1; i<=5; i++ ) {
			
			product = product * i;
			System.out.println(product);
		}

		System.out.println(product);
		
	}

}
