package day13wrapperscopewhiledt;

public class WhileLoop01 {

	public static void main(String[] args) {
		//While loop is a loop like for loop
		//They do same things with different syntax
		
		//Print the counting numbers from 1 to 10 on the console
		
		
		int i = 1;
		
		while(i<10) {
			i++; // 2 3 4 5 6 7 8 9 10 burada olursa 2 den baslar ve 10 u daconsole de gosterir
			System.out.print(i+" ");
			//i++;//buradan olursa 1 den baslar ve 9 a kadar
		}

	}

}
